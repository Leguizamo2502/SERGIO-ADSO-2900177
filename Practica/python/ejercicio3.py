def sumar(num1,num2):
    total = num1 +num2
    return total

def restar(num1,num2):
    total = num1 - num2
    return total

def multiplicar(num1,num2):
    total = num1 * num2
    return total

def dividir(num1,num2):
    total = num1 / num2
    return total

print(f"Suma: {sumar(5,7)}")
print(f"restar: {restar(8,5)}")
print(f"Multiplicar: {multiplicar(5,4)}")
print(f"Division: {dividir(20,5)}")