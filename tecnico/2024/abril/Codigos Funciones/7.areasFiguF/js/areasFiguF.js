function cuadrado(plado){
    let area
    let lado = plado;
    area=lado*lado;
    return area;
}

function rectangulo(pbase,paltura){
    let area;
    let base = pbase;
    let altura = paltura;
    area = base * altura;
    return area;
    
}
function triangulo(pbase,paltura){
    let area;
    let base = pbase;
    let altura = paltura
    area = (base * altura) / 2;
    return area;

}
