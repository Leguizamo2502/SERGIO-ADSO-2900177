function porNota(pnota,pporc){
    let nota = pnota;
    let  porc = pporc;
    let rnota;
    rnota = nota * porc;
    return rnota;
}

function suma(pnota1,pnota2,pnota3){
    let nota1 = pnota1;
    let nota2 = pnota2;
    let nota3 = pnota3;
    let sumar;
    sumar = nota1 + nota2 + nota3;
    return sumar;
}
